<?php
	class Imagens{
		private $url;
		private $cod;
		private $nome;
		private $cod_produto;
	
		function get_url(){
			return $this->url;
		}
		function set_url($url){
			$this->url = $url;
		}
		function get_cod(){
			return $this->cod;
		}
		function set_cod($cod){
			$this->cod = $cod;
		}
		function get_nome(){
			return $this->nome;
		}
		function set_nome($nome){
			$this->nome = $nome;
		}
		function get_cod_produto(){
			return $this->cod_produto;
		}
		function set_cod_produto($cod_produto){
			$this->cod_produto = $cod_produto;
		}
		
		




}


















?>